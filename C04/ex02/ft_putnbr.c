#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	a;

	if (nb == -2147483648)
		write(1, "-2147483648", 11);
	else if (nb < 0)
	{
		write(1, "-", 1);
		ft_putnbr(-nb);
	}
	else if (nb > 9)
	{
		ft_putnbr(nb / 10);
		a = nb % 10 + 48;
		write(1, &a, 1);
	}
	else if (nb <= 9)
	{
		a = nb + 48;
		write(1, &a, 1);
	}
}

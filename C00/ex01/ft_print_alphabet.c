#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_alphabet(void)
{
	char	lower;

	lower = 'a';
	while (lower <= 'z')
	{
		ft_putchar(lower);
		lower++;
	}
}
